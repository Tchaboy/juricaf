            <h1>Genèse du projet</h1>

            <p>L’<a href="http://www.ahjucaf.org/">AHJUCAF</a>, l’association des cours suprêmes judiciaires francophones, forte de cinquante institutions membres, a initié en 2004, la création d’une base de données de jurisprudence en français. La version développée entre 2004 et le 15 juin 2011 est accessible à cette adresse : <a href="http://v1.juricaf.org/">http://v1.juricaf.org/</a></p>

            <p>L’objectif est de permettre aux magistrats et aux professionnels du droit (avocats, notaires, huissiers, juristes) d’avoir accès gratuitement aux décisions des cours suprêmes. Sous la direction d’un magistrat à la Cour de cassation française, le projet a été lancé officiellement début 2005 au moyen d’une subvention de l’Organisation internationale de la Francophonie et du Ministère de la justice français. Accessible à l’adresse <a href="http://www.juricaf.org/">http://www.juricaf.org</a>, la base de données comprend actuellement près de 800 000 arrêts issus de quarante pays.</p>

            <h1>Objectifs généraux du projet</h1>

            <p>L’AHJUCAF a voulu répondre à la demande des professionnels du droit et des citoyens de mettre à disposition un outil permettant un accès gratuit et une meilleure compréhension des décisions de justice. Il s’agit de la contribution des plus hauts magistrats francophones pour renforcer l’État de droit et la démocratie.</p>
            <p>Notre association a constaté le retard important pris par les États francophones à se doter de bases de données juridiques. Si en Europe, la plupart des hautes juridictions disposent d’un tel outil, dans les pays ACP aucune institution ne diffuse gratuitement sa jurisprudence, contrairement aux pays de <i>Common law</i> où les décisions de justice des <i>final court of appeal</i> sont accessibles sur Internet.</p>

            <p>Ainsi, nombre de citoyens, de défenseurs des droits de l’homme, d’avocats n’ont pas ainsi accès aux décisions de justice et ne peuvent connaître le verdict des affaires rendues dans le passé.. Dans le domaine économique, les arrêts de la Cour de justice de l’Ohada sont accessibles librement , mais avec une mise à jour incertaine  et leur publication ne répond pas aux normes documentaires retardant ainsi leur exploitation par les avocats.</p>
            <p>Juricaf est destinée en priorité au monde francophone, mais est aussi aux ressortissants des pays de common law afin qu’ils puissent avoir accès à la jurisprudence rendue en français.</p>

            <h1>Objectifs spécifiques à court et moyen terme</h1>

            <h2>Réalisations du projet à court terme</h2>

            <p>Après six ans de présence sur Internet, l’Assemblée générale de l’AHJUCAF en juin 2010 a décidé la mise à niveau sur le plan documentaire  et technique de la base de données. Juricaf respectera ainsi les standards mondiaux, européens et francophones. Elle sera réalisée au moyen de logiciels libres et des outils de veille seront également proposés.<br />
            Désormais sous la direction d’un juge du Tribunal fédéral suisse, Juricaf comportera à compter d'octobre 2011 l’intégralité des décisions françaises, suisses, canadiennes et belges, les données déjà publiées dans Juricaf, ainsi que la jurisprudence accessible et réutilisable librement sur Internet.</p>

            <p>Pour les autres membres de notre association et particulièrement les cours suprêmes africaines , l’AHJUCAF soutiendra techniquement et financièrement leurs services de documentation à se doter des technologies informatiques et documentaires afin de permettre la diffusion de la jurisprudence à l’échelon mondial.</p>

            <h2>Objectifs du projet à moyen terme</h2>
            <p>Outil documentaire unique en droit comparé et gratuit sur Internet, Juricaf proposera pour ses cours suprêmes la commercialisation de la jurisprudence à l’international par la publication de recueils, de CD-ROM ou de licences de réutilisation. Dans un objectif de permettre le développement de l’informatique juridique dans les pays du Sud, les recettes générées seront affectées aux projets des cours suprêmes membres de l’AHJUCAF.</p>

            <p>Des liens avec la législation et la doctrine seront effectués, ainsi que l’intégration de thésaurus multilingues  permettant une meilleure connaissance du droit francophone, notamment à destination des professionnels du droit issus des pays de <i>Common law</i>.
            Pour les autres membres de notre association et particulièrement les cours suprêmes africaines, l’AHJUCAF soutiendra techniquement et financièrement leurs services de documentation à se doter des technologies informatiques et documentaires afin de permettre la diffusion de la jurisprudence à l’échelon mondial.</p>
